def get_counties_for_state(state_fips: str) -> list[str]:
    """
    Get list of county FIPS codes (3-digit) for a state.

    Args:
        state_fips: 2-digit state FIPS code

    Returns:
        List of 3-digit county FIPS codes
    """
    return STATE_COUNTIES.get(state_fips, [])


def normalize_state(state: str) -> str:
    """
    Convert a state identifier to its FIPS code.

    Args:
        state: State name, abbreviation, or FIPS code

    Returns:
        2-digit state FIPS code

    Raises:
        ValueError: If state cannot be recognized
    """
    state = state.strip()

    # Already a FIPS code
    if len(state) == 2 and state.isdigit():
        if state in FIPS_STATES:
            return state
        raise ValueError(f"Unknown state FIPS code: {state}")

    # State abbreviation
    if len(state) == 2 and state.upper() in STATE_ABBREVS:
        return STATE_ABBREVS[state.upper()]

    # State name
    state_lower = state.lower()
    if state_lower in STATE_NAMES:
        return STATE_NAMES[state_lower]

    raise ValueError(f"Unknown state: {state}")
